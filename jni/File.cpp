BOOL File::Open(LPCTSTR pFileName, bool bWrite)
{
	hFile = fopen(pFileName, (bWrite ? "wb+" : "rb"));

	if (hFile == NULL) return FALSE;

	if (fseek(hFile, 0, SEEK_END) != 0) {
		Close();
		return FALSE;
	}
	nSize = ftell(hFile);

	if (fseek(hFile, 0, SEEK_SET) != 0) {
		Close();
		return FALSE;
	}
	return TRUE;
}

//////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////

void File::Close()
{
	if (hFile != NULL) {
		fclose(hFile);
		hFile = NULL;
	}
}

