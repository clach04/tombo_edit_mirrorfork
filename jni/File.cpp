BOOL File::Read(LPBYTE pBuf, LPDWORD pSize)
{
	DWORD n = *pSize;
	*pSize = (DWORD)fread(pBuf, 1, n, hFile);
	if (n != *pSize) return FALSE;
	return TRUE;
}

//////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////

BOOL File::Write(const LPBYTE pBuf, DWORD nSize)
{
	DWORD n = nSize;
	nSize = (DWORD)fwrite(pBuf, 1, n, hFile);
	if (n != nSize) return FALSE;
	return TRUE;
}


